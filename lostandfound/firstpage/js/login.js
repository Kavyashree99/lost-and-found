function openForm() {
  document.getElementById("SForm").style.display = "none";
  document.getElementById("myForm").style.display = "block";
}

function closeForm() {
  document.getElementById("myForm").style.display = "none";
}
function openSForm() {
  document.getElementById("myForm").style.display = "none";
  document.getElementById("SForm").style.display = "block";
}

function closeSForm() {
  document.getElementById("SForm").style.display = "none";
}